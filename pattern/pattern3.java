import java.util.*;
class pattern3{
    public static void main(String [] args){
     Scanner sc=new Scanner(System.in);
     int M=sc.nextInt();
     
     while(M>0){
        int m=0;
        System.out.print("* ");
       while(m<(M-1)){
         System.out.print(" ");
         m++;
       }

        System.out.print("*");
        System.out.println("");
         M--;

     }
    




    }




}